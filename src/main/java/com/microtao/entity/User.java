package com.microtao.entity;

/**
 * 用户实体类
 *
 * @Author 於涛
 * @Date 2020/10/12-18:20
 */
public class User {
    private String userName;
    private int age;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
